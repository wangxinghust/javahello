public class Dione {
    public static void main(String[] args) {
        int a = 199;
        int b = 1009;
        System.out.println(a++);
        System.out.println(a);
        System.out.println(++a);
        System.out.println(b--);
        System.out.println(b);
        System.out.println(--b);
    }
}