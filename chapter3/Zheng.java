public class Zheng {
    public static void main(String[] args) {
        // 正方形
        int b = 7;
        int L = b * 4;
        int s = b * b;
        System.out.println("The perimeter of the square is " + L);
        System.out.println("The area of the square is " + s);
        // 三角形
        int a3 = 5, b3 = 7;
        int s3 = a3 * b3 / 2;
        System.out.println("The area of the triangle is " + s3);
    }
}