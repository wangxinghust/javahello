public class IfDuo {
    public static void main(String[] args) {
        int totalPoint = 452;
        if (totalPoint > 610) {
            System.out.println("well-known colleague");
        } else if (totalPoint > 570) {
            System.out.println("general university");
        } else if (totalPoint > 450) {
            System.out.println("junior college");
        } else if (totalPoint > 390) {
            System.out.println("higher vocational school");
        } else {
            System.out.println("get the job");
        }
        System.out.println("check off");
    }
}