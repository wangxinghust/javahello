public class switch1 {
    public static void main(String[] args) {
        int a = 1997;
        switch (a) {
            case 1992:
                System.out.println("a=1992");
            default:
                System.out.println("no");
            case 1997:
                System.out.println("a=1997");
            case 2008:
                System.out.println("a=2008");
        }
    }
}