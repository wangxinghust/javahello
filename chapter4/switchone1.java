public class switchone1 {
    public static void main(String[] args) {
        int a = 11;
        switch (a) {
            case 11:
                System.out.println("a=11");
            case 22:
                System.out.println("a=22");
            case 33:
                System.out.println("a=33");
                break;
            default:
                System.out.println("no");
        }
    }
}