public class switchtest {
    public static void main(String[] args) {
        int a = 567;
        switch (a) {
            case 555:
                System.out.println("a=555");
                break;
            case 557:
                System.out.println("a=557");
                break;
            case 567:
                System.out.println("a=567");
                break;
            default:
                System.out.println("no");
        }
    }
}